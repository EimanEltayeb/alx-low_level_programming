#include "3-calc.h"
/**
 * op_add - add
 * op_sub - sub
 * op_mul - mult
 * op_div - div
 * op_mod - mod
 *@a: num1
 *@b: num2
 *Return: result
 */

int op_add(int a, int b)
{
	return(a + b);
}
int op_sub(int a, int b)
{
	return (a - b);
}

int op_mul(int a, int b)
{
	return (a * b);
}

int op_div(int a, int b)
{
	return (a / b);
}
int op_mod(int a, int b)
{
	return (a % b);
}
